// I learned we can pass values into a function with arguments. You can use a return statement to send a value back out of a function.

// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line
function timesFive(num) {
  return num * 5;
}
console.log(timesFive(5));

console.log(minusSeven(10));
